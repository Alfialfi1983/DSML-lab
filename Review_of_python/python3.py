print("demo of basic data type :string")
s="hello"
t="world"
print("string 1: ",s)
print("string 2:",t)
print("string concatenation:",s+t)
print("capitalise",s+t.capitalize())
print("convert to uppercase:",s.upper())
print("right justify a string",s.rjust(7))
print("string at center:",s.center(7))
print("after replacing 'l' with 'ell' :",s.replace('l','ell'))
print("string after stripping leadind and trailing white spaces:",'world'.strip())